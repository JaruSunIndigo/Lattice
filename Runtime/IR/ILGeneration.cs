        // Logs a node's errors if it returned an exception. 
        public static void LogNodeErrors(IRExecution execution, int nodeIdx, Entity qualifier, Exception exceptionValue)
        {
            IRNode node = execution.Compilation.Graph.Nodes[nodeIdx];

            if (!node.DoNotLogErrors)
            {
                // Only log an error if the exception originated from this node. If the exception is InputNodeException
                // it originated from an earlier node in this node's input graph.
                if (exceptionValue != null && exceptionValue is not InputNodeException)
                {
                    Debug.LogError($"Lattice node error: [{node}:{qualifier}]", execution.Compilation.Graph.GetOwner(node)?.Root);
                    Debug.LogException(exceptionValue, execution.Compilation.Graph.GetOwner(node)?.Root);
                }
            }
        }

        // Stores the output value of a node into the ECS state for this entity. Necessary if this node is depended on
        // in the next frame. Entity qualifier can be Entity.Null if this node is global.
        public static void StoreNodeIntoEcsState<T>(IRExecution execution, int nodeIdx, Entity qualifier, T outputValue, Exception exceptionValue)
        {
            // todo(perf): Boxing. Will likely need state struct generation to avoid.
            IRNode node = execution.Compilation.Graph.Nodes[nodeIdx];

            if (exceptionValue != null)
            {
                execution.StateDict[qualifier].SetValue(node.Id, exceptionValue);
            }
            else
            {
                execution.StateDict[qualifier].SetValue(node.Id, outputValue);
            }
        }

        /// <summary>
        ///     Emits the given constant value as IL. This mirrors the constant emission process for default parameters in C#,
        ///     as determined via SharpLab. This is a little challenging, because we have to take a runtime object value and reduce
        ///     it down to IL instructions. Luckily, the set of constants in C# is rather limited, so it's not too complex.
        /// </summary>
        /// <remarks>This supports all C# types except decimal, nint, and nuint.</remarks>
        public static void EmitOptionalParamConstant(GroboIL emit, Type constantType, object constantValue)
        {
            if (constantType == typeof(decimal) || constantType == typeof(nint) || constantType == typeof(nuint))
            {
                throw new Exception($"Type not supported as optional parameter: [{constantType}].");
            }

            // This should cover all cases, although I suspect there may be missed logic.
            // The only type we don't support is 'decimal'.

            // Convert enums to their underlying integral type first.
            if (constantType.IsEnum)
            {
                Type underlyingType = Enum.GetUnderlyingType(constantType);
                object underlyingValue = Convert.ChangeType(constantValue, underlyingType);
                Assert.IsTrue(constantValue.GetType() == constantType || constantValue.GetType() == underlyingType);
                EmitOptionalParamConstant(emit, underlyingType, underlyingValue);
                return;
            }

            // Nullables either load (pass through), or they emit the constructor for their inner type and wrap it 
            // with a nullable. This is only supported for primitives in C#.
            if (constantType.IsNullable())
            {
                if (constantValue != null)
                {
                    Assert.IsFalse(constantType.IsPrimitive); // I don't think it's possible to define a non-null non-primitive optional default in C#.
                    Type innerType = constantType.GetGenericArguments()[0];
                    Assert.IsTrue(constantValue.GetType() == innerType // A boxed (object) nullable should be represented at runtime as its inner type.
                                  || innerType.IsEnum); // A nullable enum will have an inner value of the underlying type, weirdly enough. :\
                    EmitOptionalParamConstant(emit, innerType, constantValue);
                    emit.Newobj(constantType.GetConstructor(new[] { innerType }));
                    return;
                }
                // pass through to the value type handler.
            }

            // Primitive types that convert to I4 for IL.
            // (https://learn.microsoft.com/en-us/dotnet/csharp/language-reference/builtin-types/integral-numeric-types)
            if (constantType == typeof(sbyte)
                || constantType == typeof(byte)
                || constantType == typeof(short)
                || constantType == typeof(ushort)
                || constantType == typeof(int)
                || constantType == typeof(uint)
                || constantType == typeof(char)
                || constantType == typeof(bool)) // bools are an I4, either 1 or 0.
            {
                emit.Ldc_I4(PrimitiveNumericToSignedInt(constantValue));
                return;
            }

            // Primitive types that convert to I8 for IL
            if (constantType == typeof(long) || constantType == typeof(ulong))
            {
                emit.Ldc_I8(PrimitiveNumericToSignedLong(constantValue));
                return;
            }

            if (constantType == typeof(float))
            {
                emit.Ldc_R4((float)constantValue!);
                return;
            }

            if (constantType == typeof(double))
            {
                emit.Ldc_R8((double)constantValue!);
                return;
            }

            if (constantType == typeof(string))
            {
                emit.Ldstr((string)constantValue!);
                return;
            }

            // For structs and tuples, we only support default. As far as I understand, this how it works in C#.
            // We can load the default simply by initobj'ing it.
            if (constantType.IsValueType)
            {
                if (constantValue != null)
                {
                    // Default is signified in ParameterInfo.DefaultValue with 'null'
                    throw new Exception("Struct and tuple types only support 'default' as their value in an optional parameter.");
                }

                Local tmpLocal = emit.DeclareLocal(constantType, "tmpConstant");
                emit.Ldloca(tmpLocal);
                emit.Initobj(constantType);
                emit.Ldloc(tmpLocal);
                return;
            }

            // Type must be a non-string reference type. Load null.
            if (constantValue != null)
            {
                throw new Exception($"Unsupported default parameter value: [{constantValue}].");
            }
            emit.Ldnull();
        }

        /// <summary>Converts a primitive numeric value to the representation used in IL. (ie. I4)</summary>
        private static int PrimitiveNumericToSignedInt(object o)
        {
            // Makes use of auto cast to int on return.
            // Weirdly enough casting o directly throws an exception. (ie. return (int)o)
            return o switch
            {
                bool b => b ? 1 : 0,
                sbyte sb => sb,
                byte b => b,
                short s => s,
                ushort us => us,
                int i => i,
                uint u => unchecked((int)u), // uint is loaded onto the stack as an int! weird.
                char c => c,
                _ => throw new Exception($"Unsupported type: [{o.GetType()}]")
            };
        }

        /// <summary>Converts a primitive numeric long value to the representation used in IL. (ie. I8)</summary>
        private static long PrimitiveNumericToSignedLong(object o)
        {
            // Makes use of auto cast to int on return.
            // Weirdly enough casting o directly throws an exception. (ie. return (int)o)
            return o switch
            {
                long l => l,
                ulong ul => unchecked((long)ul),
                _ => throw new Exception($"Unsupported type: [{o.GetType()}]")
            };
        }
    }
