        // Checks if the given exception is null, and if so, converts it to a InputNodeException and stores it in the output exception.
        // We use this helper to simplify branches and checks in the emitted IL.
        public static bool CheckInputException(IRExecution execution, int nodeIdx, Exception e, ref Exception exceptionLocal)
        {
            if (e != null)
            {
                if (e is not InputNodeException)
                {
                    IRNode node = execution.Compilation.Graph.Nodes[nodeIdx];
                    exceptionLocal = new InputNodeException(node, e);
                    return true;
                }
            }

            return false;
        }

        /// <summary>Gets the LatticeNode that created a specific IRNode.</summary>
        /// <remarks>This returns the *same* LatticeNode for all instances of a subgraph!</remarks>
        public static LatticeNode GetLatticeNode(IRExecution execution, int nodeIdx)
        {
            IRNode node = execution.Compilation.Graph.Nodes[nodeIdx];
            
            if (execution.Compilation.Graph.GetOwner(node) is not {} authoredNode)
            {
                throw new Exception($"ICE: Node has no authored node! [{node}]");
            }

            return authoredNode.Last;
        }

        /// <summary>Returns the T or List<T> local value from out cross phase local storage, onto the stack.</summary>
        public static object LoadCrossPhaseLocal(IRExecution execution, int nodeIdx, int pool)
        {
            var node = execution.Compilation.Graph.Nodes[nodeIdx];

            return pool switch
            {
                0 => execution.CrossPhaseOutputLocals[node],
                1 => execution.CrossPhaseExceptionLocals[node],
                _ => throw new ArgumentOutOfRangeException(nameof(pool), pool, null)
            };
        }

        /// <summary>Stores the given output local (T or List<T>) into the cross phase storage for this node.</summary>
        public static void StoreCrossPhaseLocal(IRExecution execution, int nodeIdx, int pool, object localval)
        {
            var node = execution.Compilation.Graph.Nodes[nodeIdx];

            switch (pool)
            {
                case 0:
                    execution.CrossPhaseOutputLocals[node] = localval;
                    return;
                case 1:
                    execution.CrossPhaseExceptionLocals[node] = localval;
                    return;
                default:
                    throw new ArgumentOutOfRangeException(nameof(pool), pool, null);
            }
        }

        /// <summary>Returns the value stored in the graph for the previous frame.</summary>
        public static T GetPreviousValue<T>(IRExecution execution, int nodeIdx, Entity entity, T defaultValue)
        {
            IRNode node = execution.Compilation.Graph.Nodes[nodeIdx];

            if (!execution.StateDict.ContainsKey(entity))
            {
                // If this hits, is the entity missing a LatticeState component, even though its graph uses state?
                Debug.LogError($"ICE: State dictionary is required for entity. [{entity}][{node}]");
                return defaultValue;
            }

            // Load the value of this node, stored from the previous frame's execution.
            if (execution.StateDict[entity].TryGetValue(node.Id, out object value))
            {
                if (value == null && typeof(T).IsNullable())
                {
                    return default; // null
                }

                if (value is T tValue)
                {
                    return tValue;
                }

                if (value is Exception)
                {
                    // todo: This fires if an Exception is propagated through state, because we don't differentiate
                    // output exceptions and exceptions in the state dictionary. This method should support piping
                    // an exception through, or (perhaps better) exceptions thrown shouldn't be written to state.
                    Debug.LogWarning($"Node threw exception in previous frame. Using default. [{node}][{typeof(T)}][{value?.GetType()}");
                }
                else
                {
                    Debug.LogWarning($"State was wrong type after Lattice hotswap. Using default. [{node}][{typeof(T)}][{value?.GetType()}");
                }
            }

            return defaultValue;
        }

        /// <summary>
        ///     Returns a pointer reference to stored state for a node. This is similar to GetPreviousValue, but more
        ///     efficient when you want to modify state.
        /// </summary>
        public static LatticeState.Wrapper<T> GetStateRef<T>(IRExecution execution, int nodeIdx, Entity entity, T defaultValue)
        {
            IRNode node = execution.Compilation.Graph.Nodes[nodeIdx];
            Assert.IsTrue(node is StateRefIRNode);

            // Load a reference to the stored state for this node.
            LatticeState latticeState = execution.StateDict[entity];
            if (latticeState.TryGetWrapper(node.Id, out LatticeState.Wrapper<T> value))
            {
                return value;
            }

            // Insert the default value, and return a reference to it.
            latticeState.SetValue(node.Id, defaultValue);
            latticeState.TryGetWrapper(node.Id, out LatticeState.Wrapper<T> v);
            return v;
        }

        /// <summary>
        ///     Looks up the lattice index of the given entity, under the given node's lattice. This is used for Qualifier
        ///     Transforms to do random access in a lattice.
        /// </summary>
        public static int LookupLatticeIndex(IRExecution execution, int nodeIdx, Entity qualifier)
        {
            IRNode node = execution.Compilation.Graph.Nodes[nodeIdx];

            Qualifier? qualifierId = execution.Compilation.CompileNode(node).Qualifier;

            if (!qualifierId.HasValue)
            {
                throw new Exception($"ICE: Node is not on a lattice. Should not call LookupLatticeIndex. [{node}]");
            }

            if (!execution.EntityToLatticeIndex[qualifierId.Value].TryGetValue(qualifier, out int latticeIdx))
            {
                if (qualifier == Entity.Null)
                {
                    throw new Exception($"ICE: The node [{node}] does not exist for at the global qualifier [{qualifier}].");
                }

                throw new Exception($"The node [{node}] does not exist for this entity [{qualifier}].");
            }

            return latticeIdx;
        }

        // Retrieves a scratch buffer that can be used to place output values of nodes in. 
        // Set pullFromExceptionPool to true, to retrieve a buffer from the exception list pool. 
        public static List<T> GetCachedLatticedList<T>(IRExecution execution, int nodeIdx, int pool)
        {
            IRNode node = execution.Compilation.Graph.Nodes[nodeIdx];
            Metadata compileData = execution.Compilation.CompileNode(node);

            IDictionary<IRNode, IList> listPool = pool switch
            {
                0 => execution.NodeOutputLists,
                1 => execution.NodeOutputExceptionLists,
                _ => throw new ArgumentOutOfRangeException(nameof(pool), pool, null)
            };

            // Create the list if it doesn't exist.
            List<T> list;
            if (listPool.TryGetValue(node, out IList outputList))
            {
                list = (List<T>)outputList;
            }
            else
            {
                list = new List<T>();
                listPool[node] = list;
            }

            // Fill the list with default values, if it's empty.
            if (list.Count == 0)
            {
                int entityCount = execution.EntitiesByQualifier[compileData.Qualifier!.Value].Length;
                for (int i = 0; i < entityCount; i++)
                {
                    list.Add(default);
                }
            }

            return list;
        }

